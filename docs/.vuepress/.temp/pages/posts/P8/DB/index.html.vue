<li>本标准适用于以燃煤、燃油、燃气和燃生物质成型燃料为燃料的单台出力65t/h及以下蒸汽锅炉、各种容量的热水锅炉及有机热载体锅炉；各种容量的层燃炉、抛煤机炉。</li>
<li>使用型煤、水煤浆、煤矸石、石油焦、油页岩等燃料的锅炉，参照本标准中燃煤锅炉排放控制要求执行。以木本植物或草本植物为燃料的锅炉，参照本标准中燃生物质成型燃料炉排放控制要求执行。其他燃料根据其形态参照本标准相应形态燃料的最严格排放控制要求执行。</li>
<li>本标准不适用于以生活垃圾、危险废物为燃料的锅炉。</li>
<li>本标准适用于在用锅炉的大气污染物排放管理，以及新建、改建、扩建锅炉建设项目的环境影响评价、环境保护设施设计、环境保护竣工验收、排污许可及其投产后的大气污染物排放管理。</li>
<li>本标准适用于法律允许的污染物排放行为。新设立污染源的选址和特殊保护区域内现有污染源的管理，按照《中华人民共和国大气污染防治法》《中华人民共和国水污染防治法》 《中华人民共和国海洋环境保护法》 《中华人民共和国固体废物污染环境防治法》 《中华人民共和国放射性污染防治法》 《中华人民共和国环境影响评价法》等法律、法规、规章的相关规定执行。</li>
