<li>本标准适用于以燃煤、燃油、燃气和燃生物质成型燃料为燃料的单台出力65t/h及以下蒸汽锅炉、各种容量的热水锅炉及有机热载体锅炉；各种容量的层燃炉、抛煤机炉。</li>
